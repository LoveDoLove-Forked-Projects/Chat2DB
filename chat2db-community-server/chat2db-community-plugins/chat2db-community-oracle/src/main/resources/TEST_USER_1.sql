CREATE OR REPLACE PROCEDURE MANAGE_ALL_TYPES (

    p_OPERATION IN VARCHAR2,

    p_TEST_ID IN NUMBER,

    p_TEST_VARCHAR2 IN VARCHAR2 DEFAULT NULL,

    p_TEST_NUMBER IN NUMBER DEFAULT NULL

) AS

BEGIN

    IF p_OPERATION = 'INSERT' THEN

        INSERT INTO ALL_TYPES_TABLE (

            TEST_ID, TEST_VARCHAR2, TEST_NUMBER

        ) VALUES (

            p_TEST_ID,

            p_TEST_VARCHAR2,

            p_TEST_NUMBER

        );

    ELSIF p_OPERATION = 'UPDATE' THEN

        UPDATE ALL_TYPES_TABLE

        SET

            TEST_VARCHAR2 = p_TEST_VARCHAR2,

            TEST_NUMBER = p_TEST_NUMBER

        WHERE TEST_ID = p_TEST_ID;

    ELSIF p_OPERATION = 'DELETE' THEN

        DELETE FROM ALL_TYPES_TABLE

        WHERE TEST_ID = p_TEST_ID;

    ELSE

        RAISE_APPLICATION_ERROR(-20001, 'Invalid operation. Use INSERT, UPDATE, or DELETE.');

    END IF;

END;
